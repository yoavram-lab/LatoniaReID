def submit_decision(
    data: Dict,
    unique_key: str,
    selected_predictions: List[str],
    entry: Dict,
) -> Tuple[Dict, str]:
    """Submit user decision for an entry.

    Args:
        data: Full batch predictions dict
        unique_key: Entry unique key
        selected_predictions: List of selected predictions (e.g., ["Top 1", "Top 3"])
        entry: Current entry dict

    Returns:
        (updated_data, feedback_message)
    """
    if not selected_predictions:
        return data, "Please select at least one option"

    if unique_key not in data:
        return data, f"Entry {unique_key} not found"

    # Handle special cases
    if "No Match" in selected_predictions:
        decision_value = "no_match"
        confirmed_registry_ids = []
    elif "Couldn't Decide" in selected_predictions:
        decision_value = "couldnt_decide"
        confirmed_registry_ids = []
    else:
        # Map selected predictions to decision value
        # Sort to ensure consistent ordering (Top 1, Top 2, Top 3)
        prediction_map = {"Top 1": "top1", "Top 2": "top2", "Top 3": "top3"}
        selected_keys = [
            prediction_map[p] for p in selected_predictions if p in prediction_map
        ]
        selected_keys.sort(key=lambda x: int(x[-1]))  # Sort by number
        decision_value = ",".join(selected_keys)

        # Extract unique registry IDs from selected predictions
        confirmed_registry_ids = []
        predictions = entry.get("predictions", [])

        for pred_key in selected_keys:
            # Map "top1" -> index 0, "top2" -> index 1, etc.
            pred_index = int(pred_key[-1]) - 1
            if pred_index < len(predictions):
                registry_id = predictions[pred_index].get("registry_id", "")
                if registry_id and registry_id not in confirmed_registry_ids:
                    confirmed_registry_ids.append(registry_id)

    if not decision_value:
        return data, "Invalid selection"

    # Update entry
    entry_data = data[unique_key]
    entry_data["decision"] = decision_value
    entry_data["status"] = "confirmed"
    entry_data["timestamp_updated"] = datetime.now().isoformat()

    # Update confirmed_registry_id field
    if confirmed_registry_ids:
        # Store as comma-separated string if multiple IDs, or single ID if only one
        entry_data["confirmed_registry_id"] = ",".join(confirmed_registry_ids)
    else:
        # For "no_match" or "couldnt_decide", set to empty string or null
        entry_data["confirmed_registry_id"] = ""

    # Save to disk
    save_batch_predictions(data)

    # Generate feedback
    feedback = f"Decision saved for {unique_key}: {decision_value}"
    if confirmed_registry_ids:
        feedback += f" (Registry ID(s): {', '.join(confirmed_registry_ids)})"

    return data, feedback


