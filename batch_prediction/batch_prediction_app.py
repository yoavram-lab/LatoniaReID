def load_batch_predictions(json_path: str = BATCH_PREDICTIONS_FILE) -> Dict:
    """Load batch predictions JSON into memory.

    Returns:
        Dict mapping unique_key -> prediction entry
    """
    if not os.path.exists(json_path):
        return {}

    with open(json_path, "r") as f:
        data = json.load(f)

    return data


def save_batch_predictions(data: Dict, json_path: str = BATCH_PREDICTIONS_FILE):
    """Save updated batch predictions JSON to disk."""
    with open(json_path, "w") as f:
        json.dump(data, f, indent=2)


def get_unique_dates(data: Dict) -> List[str]:
    """Extract sorted list of unique dates from batch predictions."""
    dates = set()
    for entry in data.values():
        dates.add(entry.get("date", ""))
    return sorted(list(dates))


def get_status_counts(data: Dict) -> Dict[str, int]:
    """Count entries by status.

    Returns:
        Dict with keys: pending, confirmed, new_id
    """
    counts = {
        "pending": 0,
        "confirmed": 0,
        "new_id": 0,
    }

    for entry in data.values():
        status = entry.get("status", "pending")
        if status in counts:
            counts[status] += 1

    return counts


