def get_original_image_path(masked_path: str) -> str:
    """Convert masked image path to original image path.

    Args:
        masked_path: Path to masked image (e.g., data/mask1/2013-12/14/IMGP0167.png)

    Returns:
        Path to original image if it exists, otherwise returns masked_path

    Examples:
        Bina: data/mask1/2013-12/14/IMGP0167.png -> data/rotated/2013-12/14/IMGP0167.jpg
        URI: data_uri/mask1/2016-12/1/IMG_1234.png -> data_uri/rotated/2016-12/1/IMG_1234.jpg
    """
    if not masked_path:
        return masked_path

    # Parse the path
    parts = Path(masked_path).parts

    # Determine dataset based on path prefix
    if parts[0] == "data":
        # Bina dataset
        base_dir = "data"
    elif parts[0] == "data_uri":
        # URI dataset
        base_dir = "data_uri"
    else:
        # Unknown format, return original
        return masked_path

    # Check if this is a masked path
    if len(parts) < 4 or not parts[1].startswith("mask"):
        # Not a masked path, return as-is
        return masked_path

    # Extract components: date, id, filename
    # Path format: data/mask1/2013-12/14/IMGP0167.png
    #              [0]  [1]   [2]    [3] [4]
    date = parts[2]
    id_part = parts[3]
    filename_with_ext = parts[4]

    # Get filename without extension
    filename_stem = Path(filename_with_ext).stem

    # Build original path with both .jpg and .JPG variants
    # Try .jpg first
    original_path_jpg = os.path.join(
        base_dir, "rotated", date, id_part, f"{filename_stem}.jpg"
    )
    if os.path.exists(original_path_jpg):
        return original_path_jpg

    # Try .JPG
    original_path_JPG = os.path.join(
        base_dir, "rotated", date, id_part, f"{filename_stem}.JPG"
    )
    if os.path.exists(original_path_JPG):
        return original_path_JPG

    # Original not found, return masked path
    return masked_path


