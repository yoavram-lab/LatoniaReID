def navigate_prev(current_idx: int) -> int:
    """Navigate to previous entry."""
    return max(0, current_idx - 1)


def navigate_next(current_idx: int, total: int) -> int:
    """Navigate to next entry."""
    return min(total - 1, current_idx + 1)


def jump_to_index(jump_value: str, total: int) -> int:
    """Jump to specific index (1-based input)."""
    try:
        target = int(jump_value) - 1  # Convert to 0-based
        return max(0, min(total - 1, target))
    except (ValueError, TypeError):
        return 0


