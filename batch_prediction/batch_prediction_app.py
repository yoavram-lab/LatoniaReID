def filter_predictions(
    data: Dict,
    date_filter: Optional[str] = None,
    status_filter: str = "All",
    search_id: Optional[str] = None,
) -> Dict:
    """Filter predictions based on criteria.

    Args:
        data: Full batch predictions dict
        date_filter: Date to filter by (e.g., "2024-08") or "All"
        status_filter: Status to filter by or "All"
        search_id: Temp ID to search for

    Returns:
        Filtered dict of predictions
    """
    filtered = {}

    # Map filter choices to status values
    status_map = {
        "All": None,
        "Pending": "pending",
        "Confirmed": "confirmed",
        "New_id": "new_id",
    }

    for unique_key, entry in data.items():
        # Date filter
        if date_filter and date_filter != "All":
            if entry.get("date") != date_filter:
                continue

        # Status filter
        if status_filter and status_filter != "All":
            target_status = status_map.get(status_filter)
            entry_status = entry.get("status", "pending")
            if target_status and entry_status != target_status:
                continue

        # Search filter - exact match only
        if search_id:
            if entry.get("temp_id", "") != search_id:
                continue

        filtered[unique_key] = entry

    return filtered


def get_entry_by_index(filtered_data: Dict, index: int) -> Optional[Tuple[str, Dict]]:
    """Get nth entry from filtered data.

    Args:
        filtered_data: Filtered predictions dict
        index: Zero-based index

    Returns:
        (unique_key, entry_dict) or (None, None) if index out of range
    """
    if not filtered_data:
        return None, None

    keys = sorted(filtered_data.keys())
    if index < 0 or index >= len(keys):
        return None, None

    unique_key = keys[index]
    return unique_key, filtered_data[unique_key]


