def update_entry_display(
    entry: Optional[Dict],
    index: int,
    total: int,
    show_original: bool = False,
) -> Tuple:
    """Update UI components to show current entry.

    Returns:
        Tuple of gr.update() objects for all display components
    """
    if entry is None:
        # No entry to show - clear all galleries with empty lists
        return (
            gr.update(
                value="## **Nothing to show** - No entries match the current filters"
            ),  # info_text
            gr.update(value=None),  # date_display
            gr.update(value=None),  # temp_id_display
            gr.update(value=[], visible=False),  # query_gallery - empty list
            gr.update(value=None, visible=False),  # top1_info
            gr.update(value=[], visible=False),  # top1_gallery - empty list
            gr.update(visible=False),  # top2_row
            gr.update(value=[]),  # query_gallery_top2 - empty list
            gr.update(value=None),  # top2_info
            gr.update(value=[]),  # top2_gallery - empty list
            gr.update(visible=False),  # top3_row
            gr.update(value=[]),  # query_gallery_top3 - empty list
            gr.update(value=None),  # top3_info
            gr.update(value=[]),  # top3_gallery - empty list
            gr.update(value=[]),  # selected_predictions
            gr.update(interactive=False),  # submit_btn
        )

    # Extract entry data
    unique_key = entry.get("unique_key", "")
    date = entry.get("date", "")
    temp_id = entry.get("temp_id", "")
    query_images = entry.get("query_images", [])  # Masked images for this entry
    predictions = entry.get("predictions", [])
    current_status = entry.get("status", "pending")
    current_decision = entry.get("decision")

    # Info text
    info_text = f"## Showing: {unique_key} (Entry {index + 1} of {total})"
    if len(predictions) == 0:
        info_text += "\n\n**No predictions available for this entry**"

    # Convert relative paths to absolute paths for Gradio
    def to_absolute_path(path):
        # First, convert masked to original if requested
        if show_original:
            path = get_original_image_path(path)

        if not os.path.isabs(path):
            abs_path = os.path.abspath(path)
        else:
            abs_path = path

        # Fix extension: gallery paths in JSON have .jpg but actual files are .png
        if abs_path.endswith(".jpg"):
            png_path = abs_path[:-4] + ".png"
            # Check if .png version exists
            if os.path.exists(png_path):
                return png_path

        return abs_path

    # Query gallery - convert to absolute paths
    query_gallery = [to_absolute_path(p) for p in query_images] if query_images else []

    # Top 1 prediction - always show if available
    top1_visible = len(predictions) > 0
    if top1_visible:
        top1 = predictions[0]
        dataset = top1.get("dataset", "Bina")
        registry_id = top1.get("registry_id", "")

        # Show appropriate ID label based on dataset
        if dataset == "Uri":
            id_label = f"temp_{registry_id}"
        else:
            id_label = registry_id

        top1_info_df = pd.DataFrame(
            {
                "Registry ID": [id_label],
                "Match Count": [top1.get("match_count", 0)],
                "Dataset": [dataset],
            }
        )
        # Convert gallery paths to absolute
        top1_gallery = [to_absolute_path(p) for p in top1.get("gallery", [])]
    else:
        top1_info_df = None
        top1_gallery = []

    # Top 2 prediction - always show if available
    top2_visible = len(predictions) > 1
    if top2_visible:
        top2 = predictions[1]
        dataset = top2.get("dataset", "Bina")
        registry_id = top2.get("registry_id", "")

        # Show appropriate ID label based on dataset
        if dataset == "Uri":
            id_label = f"temp_{registry_id}"
        else:
            id_label = registry_id

        top2_info_df = pd.DataFrame(
            {
                "Registry ID": [id_label],
                "Match Count": [top2.get("match_count", 0)],
                "Dataset": [dataset],
            }
        )
        # Convert gallery paths to absolute
        top2_gallery = [to_absolute_path(p) for p in top2.get("gallery", [])]
    else:
        top2_info_df = None
        top2_gallery = []

    # Top 3 prediction - always show if available
    top3_visible = len(predictions) > 2
    if top3_visible:
        top3 = predictions[2]
        dataset = top3.get("dataset", "Bina")
        registry_id = top3.get("registry_id", "")

        # Show appropriate ID label based on dataset
        if dataset == "Uri":
            id_label = f"temp_{registry_id}"
        else:
            id_label = registry_id

        top3_info_df = pd.DataFrame(
            {
                "Registry ID": [id_label],
                "Match Count": [top3.get("match_count", 0)],
                "Dataset": [dataset],
            }
        )
        # Convert gallery paths to absolute
        top3_gallery = [to_absolute_path(p) for p in top3.get("gallery", [])]
    else:
        top3_info_df = None
        top3_gallery = []

    # Selected predictions checkboxgroup - restore current decision
    selected_value = []
    if current_decision == "top1":
        selected_value = ["Top 1"]
    elif current_decision == "top2":
        selected_value = ["Top 2"]
    elif current_decision == "top3":
        selected_value = ["Top 3"]
    elif current_decision == "top1,top2":
        selected_value = ["Top 1", "Top 2"]
    elif current_decision == "top1,top3":
        selected_value = ["Top 1", "Top 3"]
    elif current_decision == "top2,top3":
        selected_value = ["Top 2", "Top 3"]
    elif current_decision == "top1,top2,top3":
        selected_value = ["Top 1", "Top 2", "Top 3"]
    elif current_decision == "no_match":
        selected_value = ["No Match"]
    elif current_decision == "couldnt_decide":
        selected_value = ["Couldn't Decide"]

    # Submit button - enable if entry is pending
    submit_interactive = current_status == "pending"

    return (
        gr.update(value=info_text),
        gr.update(value=date),
        gr.update(value=temp_id),
        gr.update(value=query_gallery),
        gr.update(value=top1_info_df, visible=top1_visible),
        gr.update(value=top1_gallery, visible=top1_visible),
        gr.update(visible=top2_visible),  # top2_row - show/hide entire row
        gr.update(value=query_gallery if top2_visible else None),  # query_gallery_top2
        gr.update(value=top2_info_df if top2_visible else None),  # top2_info
        gr.update(value=top2_gallery if top2_visible else None),  # top2_gallery
        gr.update(visible=top3_visible),  # top3_row - show/hide entire row
        gr.update(value=query_gallery if top3_visible else None),  # query_gallery_top3
        gr.update(value=top3_info_df if top3_visible else None),  # top3_info
        gr.update(value=top3_gallery if top3_visible else None),  # top3_gallery
        gr.update(value=selected_value),  # selected_predictions
        gr.update(interactive=submit_interactive),
    )


def update_status_counter(data: Dict) -> str:
    """Generate status counter markdown text."""
    counts = get_status_counts(data)

    text = f"**{counts['pending']} Pending** | " f"**{counts['confirmed']} Confirmed** "

    return text


